  `tenant_id` BIGINT UNSIGNED DEFAULT NULL COMMENT '所属租户ID',
  `camera_code` VARCHAR(64) NOT NULL COMMENT '摄像头编号',
  `camera_name` VARCHAR(128) NOT NULL COMMENT '摄像头名称',
  `camera_type` VARCHAR(32) NOT NULL DEFAULT 'ip_camera' COMMENT '摄像头类型：ip_camera/wifi_camera/thermal_camera',
  `vendor_name` VARCHAR(64) DEFAULT NULL COMMENT '品牌',
  `model_name` VARCHAR(64) DEFAULT NULL COMMENT '型号',
  `serial_no` VARCHAR(128) DEFAULT NULL COMMENT '序列号',
  `gateway_id` BIGINT UNSIGNED DEFAULT NULL COMMENT '所属网关ID',
  `area_id` BIGINT UNSIGNED DEFAULT NULL COMMENT '所属区域ID',
  `media_node_id` BIGINT UNSIGNED DEFAULT NULL COMMENT '关联媒体节点ID',
  `ip_address` VARCHAR(64) DEFAULT NULL COMMENT 'IP地址',
  `mac_address` VARCHAR(64) DEFAULT NULL COMMENT 'MAC地址',
  `wifi_ssid` VARCHAR(128) DEFAULT NULL COMMENT '接入WiFi名称',
  `install_position` VARCHAR(128) DEFAULT NULL COMMENT '安装位置',
  `orientation_text` VARCHAR(64) DEFAULT NULL COMMENT '朝向说明',
  `capture_mode` VARCHAR(32) NOT NULL DEFAULT 'manual' COMMENT '抓图模式：manual/schedule/event',
  `stream_protocol` VARCHAR(32) NOT NULL DEFAULT 'rtsp' COMMENT '主流协议：rtsp/rtmp/http',
  `online_status` VARCHAR(32) NOT NULL DEFAULT 'offline' COMMENT '在线状态：online/offline',
