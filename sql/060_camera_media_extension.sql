  CONSTRAINT `fk_iot_cameras_area_id` FOREIGN KEY (`area_id`) REFERENCES `biz_areas` (`id`),
  CONSTRAINT `fk_iot_cameras_media_node_id` FOREIGN KEY (`media_node_id`) REFERENCES `iot_media_nodes` (`id`),
  CONSTRAINT `fk_iot_cameras_tenant_id` FOREIGN KEY (`tenant_id`) REFERENCES `sys_tenants` (`id`)
