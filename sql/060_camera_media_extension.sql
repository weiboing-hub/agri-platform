USE `agri_iot_platform_dev`;

CREATE TABLE IF NOT EXISTS `iot_media_nodes` (
  `id` BIGINT UNSIGNED NOT NULL AUTO_INCREMENT COMMENT '媒体节点主键',
  `tenant_id` BIGINT UNSIGNED DEFAULT NULL COMMENT '所属租户ID',
  `node_code` VARCHAR(64) NOT NULL COMMENT '媒体节点编号',
  `node_name` VARCHAR(128) NOT NULL COMMENT '媒体节点名称',
  `node_type` VARCHAR(32) NOT NULL DEFAULT 'media_server' COMMENT '节点类型：media_server/storage_gateway/edge_gateway',
  `host_address` VARCHAR(255) NOT NULL COMMENT '主机地址',
  `api_base_url` VARCHAR(255) DEFAULT NULL COMMENT '媒体服务 API 地址',
  `rtmp_base_url` VARCHAR(255) DEFAULT NULL COMMENT 'RTMP 推流基础地址',
  `hls_base_url` VARCHAR(255) DEFAULT NULL COMMENT 'HLS 播放基础地址',
  `ftp_root_path` VARCHAR(255) DEFAULT NULL COMMENT 'FTP 根路径',
  `status` VARCHAR(32) NOT NULL DEFAULT 'enabled' COMMENT '状态：enabled/disabled',
  `health_status` VARCHAR(32) NOT NULL DEFAULT 'unknown' COMMENT '健康状态：unknown/healthy/warning/error',
  `last_heartbeat_at` DATETIME DEFAULT NULL COMMENT '最近心跳时间',
  `remark` VARCHAR(255) DEFAULT NULL COMMENT '备注',
  `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_iot_media_nodes_tenant_node_code` (`tenant_id`, `node_code`),
  KEY `idx_iot_media_nodes_status` (`status`, `health_status`),
  KEY `idx_iot_media_nodes_tenant_id` (`tenant_id`, `status`),
  CONSTRAINT `fk_iot_media_nodes_tenant_id` FOREIGN KEY (`tenant_id`) REFERENCES `sys_tenants` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='媒体服务节点表';

CREATE TABLE IF NOT EXISTS `iot_cameras` (
