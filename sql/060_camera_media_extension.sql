  `last_pull_at` DATETIME DEFAULT NULL COMMENT '最近拉流时间',
  `last_error_text` VARCHAR(255) DEFAULT NULL COMMENT '最近错误',
  `created_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updated_at` DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_iot_camera_streams_camera_role` (`camera_id`, `stream_role`),
  KEY `idx_iot_camera_streams_status` (`status`, `source_protocol`),
